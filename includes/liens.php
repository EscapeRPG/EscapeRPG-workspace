        <div id="quest3" class="dropquestion">Un escape game, c'est quoi ?</div>

        <div id="reponse3" class="reponse">
            <p>
                Un escape game est un jeu d'évasion grandeur nature.
                Généralement entre deux à six joueurs, le but est de sortir d'une pièce close en un temps limité en résolvant des énigmes à l'aide d'indices disséminés dans la salle.<br>
                Il s'agit donc d'un jeu de réflexion et de cohésion de groupe.<br>
                <br>
                Issu à l'origine des jeux vidéo “Point & Click”, il a depuis été adapté en jeu de société, en livres et de bien d'autres manières.
                Cette expansion de supports a naturellement mené à la création de ce site internet.
            </p>
        </div>

        <div id="quest4" class="dropquestion">Un jeu de rôle, c'est quoi ?</div>

        <div id="reponse4" class="reponse">
            <p>
                Le jeu de rôle est un jeu de société prenant généralement place autour d'une table.<br>
                Les joueurs interprètent des personnages, en vivant une aventure proposée par un autre joueur, baptisé Maître du Jeu.
                Ce dernier est chargé de préparer la trame de l'histoire, puis d'animer ou d'arbitrer la partie.<br>
                <br>
                L'aventure comporte des énigmes et des obstacles que les personnages doivent résoudre et franchir.
                Il n'y a ni gagnant ni perdant, le but étant pour les joueurs de coopérer pour mener à bien l'aventure.<br>
                Le jeu de rôle est un jeu de dialogues et d'imagination : le Maître du Jeu décrit une scène (décors, personnages non-joueurs ou PNJ (personnages non incarnés par les joueurs).
                Chaque joueur indique alors ce que fait ou tente de faire son personnage en réaction.
                Le Maître du Jeu indique alors le résultat des actions des personnages et la suite de la scène.<br>
                <br>
                Les règles de jeu fournissent un cadre : des données chiffrées permettant au joueur de savoir ce que son personnage peut faire ou non, le plus souvent en faisant appel à un lancer de dé.
            </p>
        </div>

        <div id="quest5" class="dropquestion">EscapeRPG, c'est quoi ?</div>

        <div id="reponse5" class="reponse">
            <p>
                EscapeRPG, c'est donc l'association du jeu de rôle et de l'escape game.<br>
                Vous trouverez ici des jeux où vous incarnerez un ou plusieurs personnages dans des histoires créées spécialement par notre équipe.<br>
                <br>
                Pour progresser dans les scénarios, il vous faudra enquêter dans les différents lieux qui vous seront proposés afin de collecter les indices
                et informations vous permettant de résoudre les énigmes qui jalonnent le parcours.<br>
                Contrairement aux escape games classiques, vous ne disposez généralement pas d'un temps limité pour arriver au bout de vos enquêtes,
                prenez donc le temps de bien explorer les différentes possibilités qui s'offrent à vous et de vous imprégner de l'histoire.
            </p>
        </div>

        <div id="quest6" class="dropquestion">Combien de temps prend la création d'un scénario ?</div>

        <div id="reponse6" class="reponse">
            <p>
                Travaillant exclusivement sur notre temps libre, la création des scénarios demande du temps.
                Les différentes phases du processus menant à la sortie d'un nouveau jeu sont les suivantes :<br>
                <br>
                - Écriture du scénario dans les grandes lignes.</br>
                - Conceptualisation et création des énigmes.</br>
                - Écriture du script.</br>
                - Croquis des visuels nécessaires au jeu.</br>
                - Codage informatique.</br>
                - Phase de tests.</br>
                - Éventuelles rectifications.</br>
                - Finalisation des visuels.</br>
                - Ajout d'éventuels visuels supplémentaires.</br>
                <br>
                Tenant compte que chacune des étapes est plus chronophage que la précédente, il n'est pas rare que la publication d'une nouvelle histoire prenne plusieurs mois,
                en général entre 6 et 12 mois environ.
            </p>
        </div>

